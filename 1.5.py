#This program add,subtract,multiply and divides two numbers
a = float(input("Enter a number:"))
b = float(input("Enter another number:"))
      
def add():
      c = a + b
      print("Add:",c)

def sub():
      c = a - b
      print("Sub",c)

def multi():
      c = a * b
      print("Multi",c)
      
def div():
      c = a / b
      print("Div",c)

add()
sub()
multi()
div()
